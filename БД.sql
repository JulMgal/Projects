DROP DATABASE IF EXISTS HOTEL;
CREATE DATABASE HOTEL;
USE HOTEL;

DROP TABLE IF EXISTS room;
CREATE TABLE room (
	id SERIAL PRIMARY KEY,
	number_of_room VARCHAR(50) UNIQUE,
    room_category VARCHAR(50),
    INDEX room_num_idx(number_of_room),
    INDEX room_cat_idx(room_category)
);

DROP TABLE IF EXISTS room_category;
CREATE TABLE room_category(
	id_room_category SERIAL PRIMARY KEY,
	category ENUM ('SGL', 'DBL', 'TWIN', 'TRPL', 'QDPL'),
	quantity_places INT,
	quantity_rooms INT,
	price DECIMAL,
	INDEX quantity_places_idx(quantity_places),
    INDEX price_idx(price),
    FOREIGN KEY (id_room_category) REFERENCES room(id)
);

DROP TABLE IF EXISTS staff;
CREATE TABLE staff (
	id SERIAL PRIMARY KEY,
	firstname VARCHAR(50),
    lastname VARCHAR(50), 
    email VARCHAR(120),
    phone BIGINT,
    post VARCHAR(50),
    salary DECIMAL,
    INDEX staff_phone_idx(phone),
    INDEX staff_firstname_lastname_idx(firstname, lastname)
);

DROP TABLE IF EXISTS reservation;
CREATE TABLE reservation (
	id SERIAL PRIMARY KEY,
    `names` TEXT,
	INDEX reservation_idx(id)
);

DROP TABLE IF EXISTS resident;
CREATE TABLE resident (
	id SERIAL PRIMARY KEY,
    firstname VARCHAR(50),
    lastname VARCHAR(50), 
    email VARCHAR(120),
    phone BIGINT,
    INDEX resident_phone_idx(phone),
    INDEX resident_firstname_lastname_idx(firstname, lastname),
    FOREIGN KEY (id) REFERENCES reservation(id)
);

DROP TABLE IF EXISTS room_stat;
CREATE TABLE room_stat (
	id_room BIGINT UNSIGNED NOT NULL,
    id_resident BIGINT UNSIGNED NOT NULL,
	id_staff BIGINT UNSIGNED NOT NULL,
    reservation_id BIGINT UNSIGNED NOT NULL,
    reservation TIMESTAMP,
	entrance TIMESTAMP,
	leaving TIMESTAMP,
	amount DECIMAL,
	FOREIGN KEY (id_staff) REFERENCES staff(id),
    FOREIGN KEY (reservation_id) REFERENCES reservation(id),
    FOREIGN KEY (id_room) REFERENCES room(id),
    FOREIGN KEY (id_resident) REFERENCES resident(id)
);

DROP TABLE IF EXISTS profile_staff;
CREATE TABLE profile_staff (
	id_staff SERIAL PRIMARY KEY,
	gender CHAR(1),
	birthday DATE,
    education ENUM ('School Education', 'Higher Education', 'Undergraduate level', 'Graduate level', 'Postgraduate level', 'Senior doctorate level'),
    photo_id BIGINT UNSIGNED NULL,
    hometown VARCHAR(100),
    FOREIGN KEY (id_staff) REFERENCES staff(id)
    	ON UPDATE CASCADE 
    	ON DELETE restrict
);

DROP TABLE IF EXISTS post;
CREATE TABLE post (
	id_post SERIAL PRIMARY KEY,
	post VARCHAR(50),
	responsibilities VARCHAR(1000),
	FOREIGN KEY (id_post) REFERENCES staff(id)
);

DROP TABLE IF EXISTS profile_res;
CREATE TABLE profile_res (
	id_resident SERIAL PRIMARY KEY,
    gender CHAR(1),
    birthday DATE,
	photo_id BIGINT UNSIGNED NULL,
    hometown VARCHAR(100),
    FOREIGN KEY (id_resident) REFERENCES resident(id) 
    	ON UPDATE CASCADE 
    	ON DELETE restrict
);

DROP TABLE IF EXISTS breakfast;
CREATE TABLE breakfast (
    id_room SERIAL PRIMARY KEY,
	quantity_res BIGINT,
	order_br VARCHAR(1000),
	price DECIMAL,
	INDEX price_idx(price),
    FOREIGN KEY (id_room) REFERENCES room(id)
);

